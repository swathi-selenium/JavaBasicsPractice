package Introduction;

public class ArithmeticOperators {

	
	public static void main(String[] args) {
		
		int a =10;
		int b =20;
		int c =30;
		int d =40;
		
		System.out.println("a + b = "+ (a + b));
		System.out.println("a - b = "+ (a - b));
		System.out.println("a * b = "+ (a * b));
		System.out.println("a / b = "+ (a / b));
		System.out.println("a % b = "+ (a % b));
		System.out.println("c++ =" + (c++));//pre increment-original value
		System.out.println("d-- =" + (d--));//pre decrement-original value
		System.out.println("++c =" + (++c));//post increment-new value
		System.out.println("--d =" + (--d));//post decrement-new value
		
		
		
		
		

	}

}
